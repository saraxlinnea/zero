import { readdirSync, writeFileSync } from "node:fs";
import { join, dirname } from "node:path";
import { fileURLToPath } from "node:url";
import { getTakenDate, selectSources } from "./photo-utils.mjs";

const root = join(dirname(fileURLToPath(import.meta.url)), "..");
const photosDir = join(root, "public", "photos");
const srcDir = join(root, "Photos");

/** March 15 play burst — Character tab animation; only one stays in gallery. */
const MARCH15_PLAY_FILES = [
  "IMG_0113.jpg",
  "IMG_0114.jpg",
  "IMG_0115.jpg",
  "IMG_0116.jpg",
  "IMG_0117.jpg",
];
const GALLERY_MARCH15_KEEP = "IMG_0113.jpg";

const { selected: sources } = selectSources(srcDir);
const sourceDates = new Map(sources.map((s) => [s.outName, s.taken]));

const files = readdirSync(photosDir)
  .filter((f) => /\.jpe?g$/i.test(f))
  .sort((a, b) => a.localeCompare(b, undefined, { numeric: true }));

const photos = files.map((file) => {
  const taken = sourceDates.get(file) ?? getTakenDate(join(photosDir, file));
  if (!taken) throw new Error(`No creation date for ${file}`);
  return { file, taken };
});

const playSequence = MARCH15_PLAY_FILES
  .filter((file) => photos.some((p) => p.file === file))
  .map((file) => photos.find((p) => p.file === file));

const galleryExclude = new Set(
  MARCH15_PLAY_FILES.filter((f) => f !== GALLERY_MARCH15_KEEP),
);

const galleryPhotos = photos.filter((p) => !galleryExclude.has(p.file));

const out = join(root, "src", "photos.js");
writeFileSync(
  out,
  `// Auto-generated by scripts/generate-photo-manifest.mjs — do not edit by hand
export const PHOTOS = ${JSON.stringify(photos, null, 2)};

export const GALLERY_PHOTOS = ${JSON.stringify(galleryPhotos, null, 2)};

export const PLAY_SEQUENCE = ${JSON.stringify(playSequence, null, 2)};
`,
);
console.log(`Wrote ${photos.length} photos, ${galleryPhotos.length} gallery, ${playSequence.length} play frames to src/photos.js`);
