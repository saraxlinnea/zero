import { readdirSync, writeFileSync } from "node:fs";
import { join, dirname } from "node:path";
import { fileURLToPath } from "node:url";

const root = join(dirname(fileURLToPath(import.meta.url)), "..");
const photosDir = join(root, "public", "photos");
const files = readdirSync(photosDir)
  .filter((f) => /\.jpe?g$/i.test(f))
  .sort((a, b) => a.localeCompare(b, undefined, { numeric: true }));

const out = join(root, "src", "photos.js");
writeFileSync(
  out,
  `// Auto-generated by scripts/generate-photo-manifest.mjs — do not edit by hand\nexport const PHOTOS = ${JSON.stringify(files, null, 2)};\n`
);
console.log(`Wrote ${files.length} entries to src/photos.js`);
