import { execSync } from "node:child_process";
import { readdirSync, writeFileSync } from "node:fs";
import { join, dirname } from "node:path";
import { fileURLToPath } from "node:url";
import { getTakenDate, getSourceDateForOutput } from "./photo-utils.mjs";

const root = join(dirname(fileURLToPath(import.meta.url)), "..");
const photosDir = join(root, "public", "photos");
const srcDir = join(root, "Photos");

const files = readdirSync(photosDir)
  .filter((f) => /\.jpe?g$/i.test(f))
  .sort((a, b) => a.localeCompare(b, undefined, { numeric: true }));

const photos = files.map((file) => {
  const fromSource = getSourceDateForOutput(srcDir, file);
  const fromOutput = getTakenDate(join(photosDir, file));
  const taken = fromSource ?? fromOutput;
  if (!taken) throw new Error(`No creation date for ${file}`);
  return { file, taken };
});

const out = join(root, "src", "photos.js");
writeFileSync(
  out,
  `// Auto-generated by scripts/generate-photo-manifest.mjs — do not edit by hand\nexport const PHOTOS = ${JSON.stringify(photos, null, 2)};\n`
);
console.log(`Wrote ${photos.length} entries to src/photos.js`);
